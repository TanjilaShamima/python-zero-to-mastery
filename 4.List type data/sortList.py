# sort list in python

testNumbers = [11, 2, 3, 24, 5, 6, 7, 18, 9, 10]

# # sort list in python
# # sort list in ascending order
testNumbers.sort()
print(f"Sorted list in ascending order: {testNumbers}")

# # sort list in descending order
testNumbers.sort(reverse=True)
print(f"Sorted list in descending order: {testNumbers}")