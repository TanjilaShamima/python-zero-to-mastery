# Method in tuple

testTuple = (1, 2, 3, 4, 5, 2, 4, 4, 6, 4, 8, 4)

# Count the number of occurrences of 4 in the tuple
count_4 = testTuple.count(4)
print(f"Count of 4 in the tuple: {count_4}")  # Output: 5

# Index the number of occurrences of 2 in the tuple

index_2 = testTuple.index(2)
print(f"Index of 2 in the tuple: {index_2}")  # Output: 1